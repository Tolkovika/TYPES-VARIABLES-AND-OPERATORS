# String manipulation
#
movie = "The Lord of the Rings: The Return of the King"
print('Number of characters: ', len(movie))
print('Title in capital letters: ', movie.upper())
print('Title in small letters: ', movie.lower())
print('Number of times "e" appears: ', movie.count('e'))
print('The word "Lord" is found at position: ', movie.find('Lord'))
print('The word "dragon" is found at position: ', movie.find('dragon'))